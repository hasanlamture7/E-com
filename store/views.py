@login_required
def minus_cart(request, cart_id):
    if request.method == 'GET':
        cp = get_object_or_404(Cart, id=cart_id)
        # Remove the Product if the quantity is already 1
        if cp.quantity == 1:
            cp.delete()
        else:
            cp.quantity -= 1
            cp.save()
    return redirect('store:cart')

@login_required
def checkout(request):
    if request.method == 'POST':
        user = request.user
        address_id = request.POST.get('address')  # Retrieve address ID from POST data

        # Ensure address exists and belongs to the logged-in user
        address = get_object_or_404(Address, id=address_id, user=user)

        # Get all the products of the user in the cart
        rawcart = Cart.objects.filter(user=user)
        if not rawcart.exists():
            messages.error(request, "Your cart is empty.")
            return redirect('store:cart')

        # Save each cart item as an order and clear the cart
        for c in rawcart:
            Order.objects.create(user=user, address=address, product=c.product, quantity=c.quantity)
            c.delete()

        cartitems = Cart.objects.filter(user=request.user)
        total_price = 0

        for item in cartitems:
            total_price += item.product.price * item.quantity
        context = {'cartitems': cartitems, 'total_price': total_price}

        return render(request, "store/checkout.html", context)
    else:
        # Handle GET request
        user = request.user
        cart_products = Cart.objects.filter(user=user)
        addresses = Address.objects.filter(user=user)
        amount = decimal.Decimal(0)
        shipping_amount = decimal.Decimal(10)
        for p in cart_products:
            temp_amount = (p.quantity * p.product.price)
            amount += temp_amount

        context = {
            'cart_products': cart_products,
            'addresses': addresses,
            'amount': amount,
            'shipping_amount': shipping_amount,
            'total_amount': amount + shipping_amount,
        }
        return render(request, 'store/checkout.html', context)


